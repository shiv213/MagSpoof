#include "WriteBufferStream.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
WriteBufferStream::WriteBufferStream(uint8_t *buff, size_t len) : BaseBufferStream(buff, len) {
}

////////////////////////////////////////////////////////////////////////////////////////////////////
size_t WriteBufferStream::write(uint8_t value) {
	if (len == 0) {
		return 0;
	}
	*buff++ = value;
	--len;
  	++sz;
	return 1;
}
